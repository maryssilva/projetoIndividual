function publicar(req, res) {
    var selectCidades = req.body.selectCidades;
    var comentario = req.body.comentario;
    var idUsuario = req.params.idUsuario;
    var idCidadeEscolhida = req.params.idCidadeEscolhida;

    if (selectCidades == '1') {
        idCidadeEscolhida = 1
    }
    else if (selectCidades == '2') {
        idCidadeEscolhida = 2
    }
    else if (selectCidades == '3') {
        idCidadeEscolhida = 3
    }
    else if (selectCidades == '4') {
        idCidadeEscolhida = 4
    }

    if (selectCidades == undefined) {
        res.status(400).send("A seleção está indefinida!");
    } else if (comentario == undefined) {
        res.status(400).send("O comentário está indefinido!");
    } else if (idUsuario == undefined) {
        res.status(403).send("O id do usuário está indefinido!");
    } else {
        avisoModel.publicar(selectCidades, comentario, idUsuario)
            .then(
                function (resultado) {
                    res.json(resultado);
                }
            )
            .catch(
                function (erro) {
                    console.log(erro);
                    console.log("Houve um erro ao realizar o post: ", erro.sqlMessage);
                    res.status(500).json(erro.sqlMessage);
                }
            );
    }
}

